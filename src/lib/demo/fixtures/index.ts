function attachPatientPayer(
	claim: Omit<DemoState["claims"][0], "patient" | "payer">
): DemoState["claims"][0] {
	const patient = patients.find((p) => p.id === claim.patientId);
	const payer = payers.find((p) => p.id === claim.payerId);
	return {
		...claim,
		patient,
		payer,
		mrn: patient?.mrn,
	};
}

